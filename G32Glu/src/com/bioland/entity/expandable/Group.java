package com.bioland.entity.expandable;

public class Group {

	private String normal;
	private String highSide;
	private String lowSide;

	public String getNormal() {
		return normal;
	}

	public void setNormal(String normal) {
		this.normal = normal;
	}

	public String getHighSide() {
		return highSide;
	}

	public void setHighSide(String highSide) {
		this.highSide = highSide;
	}

	public String getLowSide() {
		return lowSide;
	}

	public void setLowSide(String lowSide) {
		this.lowSide = lowSide;
	}

}
